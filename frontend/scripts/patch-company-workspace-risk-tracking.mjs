function main() {
  const files = fs.readdirSync(MESSAGES).filter((f) => f.endsWith(".json") && !f.includes("bundle"));
  for (const f of files) {
    const p = path.join(MESSAGES, f);
    const data = JSON.parse(fs.readFileSync(p, "utf8"));
    if (!data.companyWorkspace) continue;
    const loc = f.replace(/\.json$/, "");
    if (loc === "tr") {
      data.companyWorkspace.risk = RISK_TR;
      data.companyWorkspace.tracking = TRACKING_TR;
    } else {
      data.companyWorkspace.risk = JSON.parse(JSON.stringify(RISK_EN));
      data.companyWorkspace.tracking = JSON.parse(JSON.stringify(TRACKING_EN));
    }
    fs.writeFileSync(p, `${JSON.stringify(data, null, 2)}\n`);
    console.log("patched", f);
  }
}

main();
